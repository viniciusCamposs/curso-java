package cursojava.aula13;

public class OperadoresRelacionais {
	
	public static void main(String[] args) {
		int var1 = 1;
		int var2 = 2;
		
		System.out.println("Var1 == var2: " + (var1 == var2));
		System.out.println("Var1 > var2: " + (var1 > var2));
		System.out.println("Var1 < var2: " + (var1 < var2));
		System.out.println("Var1 >= var2: " + (var1 >= var2));
		System.out.println("Var1 <= var2: " + (var1 <= var2));
		System.out.println("Var1 != var2: " + (var1 != var2));
		
	}
	
}
