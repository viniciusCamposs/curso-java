package cursojava.aula34.exercicios;

public class ConversaoDeUnidadesDeArea {
	
	public static double metrosParaPesQuadrados(double metros) {
		return metros*10.76;
	}
	
	public static double pesQuadradoParaCentimetrosQuadrados(double pes) {
		return pes*929;
	}
	
	public static double milhasQuadradaParaAcres(double milhas) {
		return milhas*640;
	}
	
	public static double acresParaPesQuadrados(double acres) {
		return acres*43560;
	}
	
}
