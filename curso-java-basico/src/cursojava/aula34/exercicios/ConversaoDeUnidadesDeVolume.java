package cursojava.aula34.exercicios;

public class ConversaoDeUnidadesDeVolume {

	public static double litroParaCentimentrosCubicos(double litros) {
		return litros*1000;
	}
	
	public static double metrosCubicoParaLitros(double metros) {
		return metros*1000;
	}
	
	public static double metrosCubicoParaPesCubicos(double metros) {
		return metros*35.32;
	}
	
	public static double galaoAmericanoParaPolegadasCubicas(double galoes) {
		return galoes*231;
	}
	
	public static double galaoAmericanoParaLitros(double galoes) {
		return galoes*3785;
	}
	
}
