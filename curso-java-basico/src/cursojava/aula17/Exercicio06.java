package cursojava.aula17;

public class Exercicio06 {

	public static void main(String[] args) {
		int count = 1;
		System.out.println("-=-=--=-=--=-=--=-=- OPÇÃO 1 -=-=--=-=--=-=--=-=-");
		while(count <= 20) {
			System.out.println(count);
			count++;
		}
		
		count = 1;
		
		System.out.println("-=-=--=-=--=-=--=-=-=- OPÇÃO 2 -=-=--=-=--=-=--=-=-");
		while(count <= 20) {
			System.out.print(" " + count);
			count++;
		}

	}

}
